func Test_preaperText(t *testing.T) {
	tests := []struct {
		name string
		str  string
		want string
	}{
		{
			name: "first test",
			str:  "My name is Ted",
			want: "!my name is !ted",
		},
		{
			name: "second test",
			str:  "nfjBUULef",
			want: "nfj!b!u!u!lef",
		},
	}
	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			if got := prepareText(tt.str); got != tt.want {
				//t.Errorf("prepareText() = #{got}, want#{tt.want}")
				t.Errorf("prepareText() = %v, want %v", got, tt.want)
			}
		})
	}
}

